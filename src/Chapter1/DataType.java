package Chapter1;

public class DataType {
    public static void main(String [] args) {
        double x;
        int var;
        var = 10;
        x = 10;
        System.out.println("int var" + var);
        System.out.println("double x" + x);
        System.out.println();

        var = var / 4;
        x = x / 4;
        System.out.println("var after division" + var);
        System.out.println("x after division" + x);
    }
}
